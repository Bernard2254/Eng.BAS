void desenhaTextura(float x, float y){
  glEnable(GL_TEXTURE_2D);
  int i=10, o;
      glClear(GL_COLOR_BUFFER_BIT);
      glColor4f(1,1,1,alfa);
      glBindTexture(GL_TEXTURE_2D, texturaFundoMenu);
    for(i=0.1*dimensaox; i<=dimensaox; i=i+(0.1*dimensaox))
      for(o=0.1*dimensaoy; o<=dimensaoy; o=o+(0.1*dimensaoy)){
      glBegin(GL_QUADS);
          glTexCoord2f(0, 0); glVertex3f(i-(0.1*dimensaox), o-(0.1*dimensaoy), 0);
          glTexCoord2f(1, 0); glVertex3f(i, o-(0.1*dimensaoy), 0);
          glTexCoord2f(1, 1); glVertex3f(i, o, 0);
          glTexCoord2f(0, 1); glVertex3f(i-(0.1*dimensaox), o, 0);
      glEnd();
    }
    glDisable(GL_TEXTURE_2D );
}

void desenhaBotoes(){
  colocaTextoBotao(texturaJogar,posx-(0.01*dimensaox),posy+(0.01*dimensaoy),0,0);
  colocaTextoBotao(texturaOpcoes,posx-(0.01*dimensaox),posy-(diferencay_menu-(0.01*dimensaoy)),0,0);
  colocaTextoBotao(texturaInstrucoes,posx+(-(0.01*dimensaox)+diferencax_menu),posy+(0.01*dimensaoy),0,0);
  colocaTextoBotao(texturaSair,posx+(-(0.01*dimensaox)+diferencax_menu),posy-(diferencay_menu-(0.01*dimensaoy)),0,0);
}

